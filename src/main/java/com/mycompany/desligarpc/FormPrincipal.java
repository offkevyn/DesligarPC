    private void instanciaTarefa()
    {
        tarefa = new TimerTask() {
        @Override
        public void run() {
            
            if(segundos < 0)
            {
                segundos = 60;
                minutos--;
            }
            
            if(minutos < 0)
            {
                minutos--;
                horas--;
            }  
            
            if(horas < 0)
            {
                minutos += 60;
            }
            
            lbVisorCronometro.setText(String.format("%02d:%02d:%02d", horas, minutos, segundos));
            
            segundos--;
        }
    };
    }

